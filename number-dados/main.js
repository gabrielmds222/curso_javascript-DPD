// Nesse arquivo serão mostrados exemplos de dados numéricos no javascript


// =============== NUMBER ===============

// O primeiro tipo de dado é o numérico tradicional

const num = 7;

// É possivel realizar as quatro operações matemáticas dentro do console.log

console.log(num + 3); // adição
console.log(num - 3); // subtração
console.log(num * 3); // multiplicação
console.log(num / 3); // divisão

// É possível transformar um número numa string por exemplo


console.log(num.toString())


// Também é possível descobrir o tipo de uma variável a partir do comando typeof


console.log(typeof num) // Nesse caso o resultado será que num é um number
console.log(typeof num.toString()) // Aqui, ao adicionar o .toString, o resultado será que num é uma string


// =============== BOOLEANS ===============

// Correspondem a um tipo de dado que tem valor true ou false

console.log( 2 == 2) // Nesse caso o valor será true
console.log( 2 == 3) // Nesse caso o valor será false



// =============== NULL ===============

// O valor null é um literal em JavaScript que representa um valor nulo ou "vazio"
// Por exemplo:

const x = null


